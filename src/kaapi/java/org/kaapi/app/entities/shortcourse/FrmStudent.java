package org.kaapi.app.entities.shortcourse;

public class FrmStudent {
	private int id;
	private String fullName;
	private String telephone;	
	private String email;
	private String university;
	private String year;
	private String gender;
	private String address;
	private String comment;
	private String kaUserid;
	private int kaUserId;
	public int getId() {
		return id;
	}
	public void setId(int id) {	
		this.id = id;
	}
	public String getFullName() {
		return fullName;
	}
	public void setFullName(String fullName) {
		this.fullName = fullName;
	}
	public String getTelephone() {
		return telephone;
	}
	public void setTelephone(String telephone) {
		this.telephone = telephone;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getUniversity() {
		return university;
	}
	public void setUniversity(String university) {
		this.university = university;
	}
	public String getYear() {
		return year;
	}
	public void setYear(String year) {
		this.year = year;
	}
	public String getGender() {
		return gender;
	}
	public void setGender(String gender) {
		this.gender = gender;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public String getComment() {
		return comment;
	}
	public void setComment(String comment) {
		this.comment = comment;
	}
	public String getKaUserid() {
		return kaUserid;
	}
	public void setKaUserid(String kaUserid) {
		this.kaUserid = kaUserid;
	}
	public int getKaUserId() {
		return kaUserId;
	}
	public void setKaUserId(int kaUserId) {
		this.kaUserId = kaUserId;
	}
}
